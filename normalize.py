mean_std = {
    'air': (284.68731689453125, 14.871289253234863),
    'mslet': (101504.34375, 930.8129272460938),
    'shum': (0.009193570353090763, 0.005923544988036156),
    'uwnd': (-0.2818755805492401, 5.02249813079834),
    'vwnd': (-0.35650038719177246, 4.5172119140625)
}


def normalize(a, name):
    mean, std = mean_std[name]
    return (a - mean) / std
